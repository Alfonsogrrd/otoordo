    <script>
        // Add hover effect for promo pictures
        const promoPictures = document.querySelectorAll('.promo-picture');
        promoPictures.forEach(picture => {
            picture.addEventListener('mouseenter', () => {
                picture.style.backgroundColor = '#007BFF'; // Change to primary color on hover
                picture.querySelector('span').textContent = 'Hovered!';
            });

            picture.addEventListener('mouseleave', () => {
                picture.style.backgroundColor = 'grey'; // Reset color
                picture.querySelector('span').textContent = 'Promo Picture';
            });
        });

        // Add fade-in effect for cards on scroll
        const promoCards = document.querySelectorAll('.promo-card');
        const observer = new IntersectionObserver(entries => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('fade-in');
                }
            });
        }, { threshold: 0.1 });

        promoCards.forEach(card => observer.observe(card));
    </script>

